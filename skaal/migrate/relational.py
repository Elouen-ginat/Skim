"""Alembic-driven DDL migrations for the relational tier.

State for each app lives at::

    .skaal/migrations/{app_name}/relational/{backend_label}/
        env.py
        script.py.mako
        alembic.ini
        versions/
            <revision_id>_<slug>.py

One subdirectory per *resolved relational backend*, so an app that pins one
SQLModel to SQLite and another to Postgres keeps two independent linear
histories.
"""

from __future__ import annotations

import asyncio
import io
import re
from contextlib import redirect_stdout
from dataclasses import dataclass, field
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

from skaal.migrate.engine import MigrationKind, state_dir
from skaal.relational import get_backend, is_relational_model
from skaal.types.relational import (
    RelationalMigrationOp,
    RelationalMigrationPlan,
    RelationalMigrationStatus,
    RelationalMigrationStep,
    RelationalRevision,
)

_ENV_PY = '''\
"""Auto-generated by Skaal. Do not edit by hand — regenerate via skaal migrate."""

from __future__ import annotations

import asyncio

from alembic import context
from sqlalchemy.ext.asyncio import async_engine_from_config

config = context.config
target_metadata = config.attributes.get("target_metadata")
include_tables = config.attributes.get("include_tables")


def include_object(object_, name, type_, reflected, compare_to):
    if include_tables is None:
        return True
    if type_ == "table":
        return name in include_tables
    if type_ == "index":
        table = getattr(object_, "table", None)
        return table is None or table.name in include_tables
    return True


def _do_run_migrations(connection):
    context.configure(
        connection=connection,
        target_metadata=target_metadata,
        include_object=include_object,
        render_as_batch=connection.dialect.name == "sqlite",
        compare_type=True,
    )
    with context.begin_transaction():
        context.run_migrations()


async def _run_online():
    section = config.get_section(config.config_ini_section) or {}
    engine = async_engine_from_config(section, prefix="sqlalchemy.")
    async with engine.connect() as connection:
        await connection.run_sync(_do_run_migrations)
    await engine.dispose()


def _run_offline():
    url = config.get_main_option("sqlalchemy.url") or ""
    context.configure(
        url=url,
        target_metadata=target_metadata,
        literal_binds=True,
        dialect_opts={"paramstyle": "named"},
        include_object=include_object,
        render_as_batch=url.startswith("sqlite"),
        compare_type=True,
    )
    with context.begin_transaction():
        context.run_migrations()


if context.is_offline_mode():
    _run_offline()
else:
    asyncio.run(_run_online())
'''

