@dataclass
class _BackendGroup:
    """One resolved relational backend and the models that resolve to it."""

    name: str
    backend: Any
    models: list[type] = field(default_factory=list)

    @property
    def table_names(self) -> set[str]:
        return {model.__tablename__ for model in self.models}

    def metadata(self) -> Any:
        from sqlmodel import SQLModel

        return SQLModel.metadata


def _backend_class_short(backend: Any) -> str:
    name = type(backend).__name__
    short = re.sub(r"Backend$", "", name).lower()
    return short or name.lower()


def _collect_groups(app: Any) -> list[_BackendGroup]:
    """Group registered relational SQLModels by *database*, not by backend instance.

    Multiple ``SqliteBackend(path=p, namespace=N)`` instances that share *p*
    represent the same physical database; each model just gets its own
    namespace for the KV facade. They must share one Alembic history.
    """
    groups: list[_BackendGroup] = []
    seen: dict[tuple[str, str], _BackendGroup] = {}
    for obj in app._collect_all().values():
        if not is_relational_model(obj):
            continue
        backend = get_backend(obj)
        base = _backend_class_short(backend)
        key = (base, _alembic_url(backend))
        group = seen.get(key)
        if group is None:
            existing = {g.name for g in groups}
            label = base
            counter = 2
            while label in existing:
                label = f"{base}_{counter}"
                counter += 1
            group = _BackendGroup(name=label, backend=backend)
            groups.append(group)
            seen[key] = group
        group.models.append(obj)
    return groups


def _select_groups(app: Any, backend_name: str | None) -> list[_BackendGroup]:
    groups = _collect_groups(app)
    if not groups:
        raise ValueError(f"No relational SQLModels registered on {app.name!r}.")
    if backend_name is None:
        return groups
    matched = [g for g in groups if g.name == backend_name]
    if not matched:
        available = ", ".join(sorted(g.name for g in groups))
        raise ValueError(f"No relational backend named {backend_name!r}. Available: {available}.")
    return matched


class RelationalMigrationStore:
    """The on-disk Alembic project for one ``(app, backend)`` pair."""

    def __init__(self, app_name: str, backend_label: str) -> None:
        self.app_name = app_name
        self.backend_label = backend_label
        self.root = state_dir(app_name, MigrationKind.RELATIONAL) / backend_label

    @property
    def versions_dir(self) -> Path:
        return self.root / "versions"

    @property
    def env_py(self) -> Path:
        return self.root / "env.py"

    @property
    def script_mako(self) -> Path:
        return self.root / "script.py.mako"

    @property
    def alembic_ini(self) -> Path:
        return self.root / "alembic.ini"

    def ensure(self, sqlalchemy_url: str) -> None:
        """Create or refresh the Alembic project files. Idempotent."""
        self.root.mkdir(parents=True, exist_ok=True)
        self.versions_dir.mkdir(exist_ok=True)
        if not self.env_py.exists():
            self.env_py.write_text(_ENV_PY)
        if not self.script_mako.exists():
            self.script_mako.write_text(_DEFAULT_SCRIPT_MAKO)
        # alembic.ini gets rewritten every time so a moved DB picks up the new url.
        self.alembic_ini.write_text(
            _ALEMBIC_INI.format(
                script_location=str(self.root.resolve()),
                url=sqlalchemy_url,
            )
        )


_DEFAULT_SCRIPT_MAKO = '''\
"""${message}

Revision ID: ${up_revision}
Revises: ${down_revision | comma,n}
Create Date: ${create_date}
"""
from typing import Sequence, Union

from alembic import op
import sqlalchemy as sa
import sqlmodel
${imports if imports else ""}

revision: str = ${repr(up_revision)}
down_revision: Union[str, None] = ${repr(down_revision)}
branch_labels: Union[str, Sequence[str], None] = ${repr(branch_labels)}
depends_on: Union[str, Sequence[str], None] = ${repr(depends_on)}


def upgrade() -> None:
    ${upgrades if upgrades else "pass"}


def downgrade() -> None:
    ${downgrades if downgrades else "pass"}
'''


def _alembic_url(backend: Any) -> str:
    """Return a SQLAlchemy URL string for *backend*'s relational engine."""
    if hasattr(backend, "_sqlalchemy_url"):
        return backend._sqlalchemy_url()
    if hasattr(backend, "_sqlalchemy_dsn"):
        return backend._sqlalchemy_dsn()
    raise TypeError(f"{type(backend).__name__} does not expose a SQLAlchemy URL for migrations.")


def _build_config(group: _BackendGroup, app_name: str) -> Any:
    """Materialize the on-disk project and return a ready ``alembic.Config``."""
    from alembic.config import Config as AlembicConfig

    store = RelationalMigrationStore(app_name, group.name)
    store.ensure(_alembic_url(group.backend))

    cfg = AlembicConfig(str(store.alembic_ini))
    cfg.set_main_option("script_location", str(store.root.resolve()))
    cfg.attributes["target_metadata"] = group.metadata()
    cfg.attributes["include_tables"] = group.table_names
    return cfg


